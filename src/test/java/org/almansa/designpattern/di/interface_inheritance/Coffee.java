package org.almansa.designpattern.di.interface_inheritance;

public class Coffee {
	private String name;

	private long price;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getPrice() {
		return price;
	}

	public void setPrice(long price) {
		this.price = price;
	}

}
